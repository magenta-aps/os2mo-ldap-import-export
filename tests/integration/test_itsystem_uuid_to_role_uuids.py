@pytest.fixture
async def itsystem(graphql_client: GraphQLClient) -> UUID:
    itsystem = await graphql_client.itsystem_create(
        input=ITSystemCreateInput(
            user_key="borgerpc",
            name="BorgerPC",
            validity=RAOpenValidityInput(from_="1970-01-01T00:00:00Z"),
        )
    )
    return itsystem.uuid


@pytest.mark.integration_test
async def test_no_itsystem(
    graphql_client: GraphQLClient,
) -> None:
    result = await itsystem_uuid_to_role_uuids(graphql_client, uuid4())
    assert result == set()


