@pytest.mark.integration_test
@pytest.mark.parametrize(
    "validity1,validity2,expected",
    [
        # Both past
        (
            ValidityInput(from_="1970-01-01T00:00:00Z", to="1980-01-01T00:00:00Z"),
            ValidityInput(from_="1970-01-01T00:00:00Z", to="1980-01-01T00:00:00Z"),
            set(),
        ),
        # Both present
        (
            ValidityInput(from_="1970-01-01T00:00:00Z"),
            ValidityInput(from_="1970-01-01T00:00:00Z"),
            {1, 2},
        ),
        # Both future
        (
            ValidityInput(from_="3000-01-01T00:00:00Z"),
            ValidityInput(from_="3000-01-01T00:00:00Z"),
            set(),
        ),
        # One past, one present
        (
            ValidityInput(from_="1970-01-01T00:00:00Z", to="1980-01-01T00:00:00Z"),
            ValidityInput(from_="1970-01-01T00:00:00Z"),
            {2},
        ),
        # One past, one present, flipped
        (
            ValidityInput(from_="1970-01-01T00:00:00Z"),
            ValidityInput(from_="1970-01-01T00:00:00Z", to="1980-01-01T00:00:00Z"),
            {1},
        ),
        # One past, one future
        (
            ValidityInput(from_="1970-01-01T00:00:00Z", to="1980-01-01T00:00:00Z"),
            ValidityInput(from_="3000-01-01T00:00:00Z"),
            set(),
        ),
    ],
)
async def test_present_multiple_roles_itsystem(
    graphql_client: GraphQLClient,
    role_facet: UUID,
    itsystem: UUID,
    validity1: ValidityInput,
    validity2: ValidityInput,
    expected: set[int],
) -> None:
    expected_set = set()

    role1 = await graphql_client.class_create(
        input=ClassCreateInput(
            name="admin",
            user_key="admin",
            scope="TEXT",
            facet_uuid=role_facet,
            it_system_uuid=itsystem,
            validity=validity1,
        )
    )
    if 1 in expected:
        expected_set.add(role1.uuid)

    role2 = await graphql_client.class_create(
        input=ClassCreateInput(
            name="admin",
            user_key="admin",
            scope="TEXT",
            facet_uuid=role_facet,
            it_system_uuid=itsystem,
            validity=validity2,
        )
    )
    if 2 in expected:
        expected_set.add(role2.uuid)

    result = await itsystem_uuid_to_role_uuids(graphql_client, itsystem)
    assert result == expected_set
